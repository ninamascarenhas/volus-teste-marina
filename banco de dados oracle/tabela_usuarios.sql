  CODIGO,
  NOME,
  EMAIL,
  STATUS,
  DATA_CADASTRO
FROM
  VLS_USUARIOS
WHERE
  STATUS = 'A'
  AND EXTRACT(YEAR FROM DATA_CADASTRO) = 2024
ORDER BY
  DATA_CADASTRO DESC;