  CODIGO NUMBER PRIMARY KEY,
  NOME VARCHAR2(50) NOT NULL,
  EMAIL VARCHAR2(100) NOT NULL,
  STATUS VARCHAR2(1) NOT NULL,
  DATA_CADASTRO DATE NOT NULL
